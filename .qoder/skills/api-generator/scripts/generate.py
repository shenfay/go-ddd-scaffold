def generate_router_code(entity_name, config):
    """生成 Router 代码"""
    handler_class = f"{entity_name.title()}Handler"
    
    code = f'''// Code generated by api-generator. DO NOT EDIT.
// source: {entity_name.title()}Aggregate

package http

import (
\t"github.com/gin-gonic/gin"
)

// Register{entity_name.title()}Routes 注册{entity_name.title()}路由
func Register{entity_name.title()}Routes(r *gin.RouterGroup, handler *{handler_class}) {{
\tgroup := r.Group("/{entity_name}s")
\t{{
\t\t// Public routes (no authentication required)
\t\tgroup.POST("", handler.Create{entity_name.title()})
\t\tgroup.GET("", handler.List{entity_name.title()}s)
\t\tgroup.GET("/:id", handler.Get{entity_name.title()})
\t\tgroup.PUT("/:id", handler.Update{entity_name.title()})
\t\tgroup.DELETE("/:id", handler.Delete{entity_name.title()})
\t}}
}}
'''
    
    return code


def main():
    """主函数"""
    args = parse_args()
    
    # 确定要处理的聚合列表
    aggregates = []
    if args.aggregate:
        aggregates = [args.aggregate]
    elif args.aggregates:
        aggregates = [a.strip() for a in args.aggregates.split(',')]
    else:
        print("❌ 错误：必须指定聚合根 (--aggregate 或 --aggregates)")
        sys.exit(1)
    
    # 配置
    config = {
        'output': args.output,
        'prefix': args.prefix,
        'with_validation': args.with_validation,
        'with_tests': args.with_tests,
        'auth': args.auth,
    }
    
    print(f"\n🚀 开始生成 API 端点")
    print(f"   聚合：{', '.join(aggregates)}")
    print(f"   输出：{config['output']}")
    print(f"   前缀：{config['prefix']}")
    print(f"   认证：{config['auth']}")
    print(f"   验证：{'是' if config['with_validation'] else '否'}\n")
    
    base_path = Path(config['output'])
    
    for aggregate in aggregates:
        try:
            # 验证聚合根名称
            validate_aggregate_name(aggregate)
            
            # 提取实体名称
            entity_name = extract_entity_name(aggregate)
            
            # 创建目录
            handler_dir = base_path / 'internal' / 'interfaces' / 'http' / entity_name
            handler_dir.mkdir(parents=True, exist_ok=True)
            
            # 生成 Handler
            handler_code = generate_handler_code(aggregate, entity_name, config)
            handler_file = handler_dir / f"{entity_name}_handler.go"
            with open(handler_file, 'w', encoding='utf-8') as f:
                f.write(handler_code)
            print(f"✓ 生成 Handler: {handler_file}")
            
            # 生成 DTO
            dto_dir = base_path / 'internal' / 'application' / entity_name / 'dto'
            dto_dir.mkdir(parents=True, exist_ok=True)
            dto_code = generate_dto_code(aggregate, entity_name, config)
            dto_file = dto_dir / f"{entity_name}_dto.go"
            with open(dto_file, 'w', encoding='utf-8') as f:
                f.write(dto_code)
            print(f"✓ 生成 DTO: {dto_file}")
            
            # 生成 Router
            router_code = generate_router_code(entity_name, config)
            router_file = handler_dir / f"{entity_name}_router.go"
            with open(router_file, 'w', encoding='utf-8') as f:
                f.write(router_code)
            print(f"✓ 生成 Router: {router_file}")
            
            print()
            
        except Exception as e:
            print(f"❌ 生成 {aggregate} 失败：{e}")
            import traceback
            traceback.print_exc()
            continue
    
    print("\n✅ API 端点生成完成!\n")
    print("下一步:")
    print(f"  1. 检查生成的代码：cd {base_path}")
    print(f"  2. 根据需要修改业务逻辑")
    print(f"  3. 运行 swag 生成 API 文档：swag init")
    print(f"  4. 启动应用测试：make run\n")


if __name__ == '__main__':
    main()
