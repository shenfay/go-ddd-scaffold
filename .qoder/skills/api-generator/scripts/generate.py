#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
API Generator - RESTful API 端点生成器
根据 DDD 聚合根自动生成完整的 API 端点、Handler、DTO 和 Swagger 文档
"""

import os
import sys
import argparse
import json
from pathlib import Path
from datetime import datetime


def parse_args():
    """解析命令行参数"""
    parser = argparse.ArgumentParser(
        description='RESTful API 端点生成器',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  # 生成单个聚合的 API
  %(prog)s --aggregate UserAggregate
  
  # 批量生成多个聚合
  %(prog)s --aggregates User,Order,Product
  
  # 启用 JWT 认证和验证
  %(prog)s --aggregate User --with-validation --auth jwt
  
  # 指定输出目录和前缀
  %(prog)s --aggregate Product --output ./custom/api --prefix /api/v1
        """
    )
    
    parser.add_argument(
        '--aggregate',
        type=str,
        help='聚合根名称 (如：UserAggregate)'
    )
    
    parser.add_argument(
        '--aggregates',
        type=str,
        help='多个聚合根列表，逗号分隔'
    )
    
    parser.add_argument(
        '--output',
        type=str,
        default='./generated',
        help='输出目录 (默认：./generated)'
    )
    
    parser.add_argument(
        '--prefix',
        type=str,
        default='/api/v1',
        help='API 路径前缀 (默认：/api/v1)'
    )
    
    parser.add_argument(
        '--with-validation',
        action='store_true',
        help='包含参数验证逻辑'
    )
    
    parser.add_argument(
        '--with-tests',
        action='store_true',
        help='包含测试文件'
    )
    
    parser.add_argument(
        '--auth',
        type=str,
        choices=['none', 'jwt', 'casbin'],
        default='jwt',
        help='认证方式 (默认：jwt)'
    )
    
    parser.add_argument(
        '--config',
        type=str,
        help='自定义配置文件路径'
    )
    
    return parser.parse_args()


def validate_aggregate_name(name):
    """验证聚合根名称"""
    if not name.endswith('Aggregate'):
        print(f"⚠️  警告：聚合根名称通常以 'Aggregate' 结尾 (当前：{name})")
    return True


def extract_entity_name(aggregate_name):
    """从聚合根名称提取实体名称"""
    # UserAggregate -> user
    # OrderAggregate -> order
    return aggregate_name.replace('Aggregate', '').lower()


