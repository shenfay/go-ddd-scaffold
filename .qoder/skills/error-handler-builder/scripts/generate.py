#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Error Handler Builder - 错误处理构建器
生成统一的错误处理代码和错误类型定义
"""

import os
import sys
import subprocess
import shutil
from pathlib import Path


class ColorLogger:
    """彩色日志输出"""
    
    BLUE = '\033[0;34m'
    GREEN = '\033[0;32m'
    YELLOW = '\033[1;33m'
    RED = '\033[0;31m'
    NC = '\033[0m'
    
    @classmethod
    def info(cls, msg):
        print(f"{cls.BLUE}[INFO]{cls.NC} {msg}")
    
    @classmethod
    def success(cls, msg):
        print(f"{cls.GREEN}[SUCCESS]{cls.NC} {msg}")
    
    @classmethod
    def warning(cls, msg):
        print(f"{cls.YELLOW}[WARNING]{cls.NC} {msg}")
    
    @classmethod
    def error(cls, msg):
        print(f"{cls.RED}[ERROR]{cls.NC} {msg}")


def get_project_root():
    """动态获取项目根目录"""
    script_dir = Path(__file__).parent
    return (script_dir / '../../../..').resolve()


def check_dependencies():
    """检查依赖工具"""
    ColorLogger.info("检查必要的工具...")
    
    missing_tools = []
    
    # 检查 go
    if not shutil.which('go'):
        missing_tools.append('go')
    
    if missing_tools:
        ColorLogger.error(f"缺少必要的工具：{' '.join(missing_tools)}")
        ColorLogger.info("请安装缺失的工具后再继续")
        sys.exit(1)
    
    ColorLogger.success("所有依赖工具检查通过")


def validate_project_structure(project_root):
    """验证 Go 项目结构"""
    ColorLogger.info("验证项目结构...")
    
    required_paths = [
        project_root / "backend" / "go.mod",
        project_root / "backend" / "internal" / "pkg" / "errors",
    ]
    
    for path in required_paths:
        if not path.exists():
            ColorLogger.error(f"项目结构不完整：缺少 {path.relative_to(project_root)}")
            return False
    
    ColorLogger.success("项目结构验证通过")
    return True


def generate_error_types(domain='', output_dir=None, project_root=None):
    """生成错误类型定义"""
    if project_root is None:
        project_root = get_project_root()
    
    if output_dir is None:
        output_dir = project_root / "backend" / "internal" / "pkg" / "errors"
    
    ColorLogger.info(f"生成错误类型定义...")
    
    # 创建输出目录
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # 生成错误代码文件
    error_code = f'''// Code generated by error-handler-builder. DO NOT EDIT.
// source: {domain or 'common'}

package errors

// {domain.title().replace('_', '')}ErrorCode {domain} 错误码
type {domain.title().replace('_', '')}ErrorCode string

const (
\t// Err{domain.title().replace('_', '')}Unknown 未知错误
\tErr{domain.title().replace('_', '')}Unknown {domain.title().replace('_', '')}ErrorCode = "{domain}_unknown"
\t
\t// Err{domain.title().replace('_', '')}NotFound 未找到
\tErr{domain.title().replace('_', '')}NotFound {domain.title().replace('_', '')}ErrorCode = "{domain}_not_found"
\t
\t// Err{domain.title().replace('_', '')}Invalid 无效参数
\tErr{domain.title().replace('_', '')}Invalid {domain.title().replace('_', '')}ErrorCode = "{domain}_invalid"
\t
\t// Err{domain.title().replace('_', '')}AlreadyExists 已存在
\tErr{domain.title().replace('_', '')}AlreadyExists {domain.title().replace('_', '')}ErrorCode = "{domain}_already_exists"
)

// Error 实现 error 接口
func (c {domain.title().replace('_', '')}ErrorCode) Error() string {{
\treturn string(c)
}}
'''
    
    filename = f"{domain}_error.go" if domain else "error_codes.go"
    with open(output_dir / filename, 'w', encoding='utf-8') as f:
        f.write(error_code)
    
    ColorLogger.success(f"生成错误类型：{filename}")
    return True


def generate_error_handler(output_dir=None, project_root=None):
    """生成错误处理器"""
    if project_root is None:
        project_root = get_project_root()
    
    if output_dir is None:
        output_dir = project_root / "backend" / "internal" / "pkg" / "errors"
    
    ColorLogger.info("生成错误处理器...")
    
    handler_code = '''// Code generated by error-handler-builder. DO NOT EDIT.
// 统一错误处理器

package errors

import (
\t"fmt"
\t"net/http"
)

// ErrorCode 错误码接口
type ErrorCode interface {
\terror
\tCode() string
}

// ErrorInfo 错误信息
type ErrorInfo struct {
\tCode    string      `json:"code"`
\tMessage string      `json:"message"`
\tDetails interface{} `json:"details,omitempty"`
}

// APIError API 错误
type APIError struct {
\tHTTPStatus int         `json:"-"`
\tCode       string      `json:"code"`
\tMessage    string      `json:"message"`
\tDetails    interface{} `json:"details,omitempty"`
}

// Error 实现 error 接口
func (e *APIError) Error() string {
\treturn fmt.Sprintf("%s: %s", e.Code, e.Message)
}

// NewAPIError 创建 API 错误
func NewAPIError(status int, code, message string, details ...interface{}) *APIError {
\terr := &APIError{
\t\tHTTPStatus: status,
\t\tCode:       code,
\t\tMessage:    message,
\t}
\t
\tif len(details) > 0 {
\t\terr.Details = details[0]
\t}
\t
\treturn err
}

// ToResponse 转换为响应格式
func (e *APIError) ToResponse() ErrorInfo {
\treturn ErrorInfo{
\t\tCode:    e.Code,
\t\tMessage: e.Message,
\t\tDetails: e.Details,
\t}
}

// 常用错误构造函数

// NotFound 资源不存在错误
func NotFound(code, message string, details ...interface{}) *APIError {
\treturn NewAPIError(http.StatusNotFound, code, message, details...)
}

// BadRequest 请求参数错误
func BadRequest(code, message string, details ...interface{}) *APIError {
\treturn NewAPIError(http.StatusBadRequest, code, message, details...)
}

// Unauthorized 未授权
func Unauthorized(code, message string, details ...interface{}) *APIError {
\treturn NewAPIError(http.StatusUnauthorized, code, message, details...)
}

// Forbidden 禁止访问
func Forbidden(code, message string, details ...interface{}) *APIError {
\treturn NewAPIError(http.StatusForbidden, code, message, details...)
}

// InternalServer 服务器内部错误
func InternalServer(code, message string, details ...interface{}) *APIError {
\treturn NewAPIError(http.StatusInternalServerError, code, message, details...)
}
'''
    
    with open(output_dir / 'handler.go', 'w', encoding='utf-8') as f:
        f.write(handler_code)
    
    ColorLogger.success("生成错误处理器：handler.go")
    return True


def list_error_types(project_root=None):
    """列出所有错误类型"""
    if project_root is None:
        project_root = get_project_root()
    
    errors_dir = project_root / "backend" / "internal" / "pkg" / "errors"
    
    if not errors_dir.exists():
        ColorLogger.error("错误目录不存在")
        return
    
    ColorLogger.info("已定义的错误类型:")
    
    error_files = list(errors_dir.glob('*_error.go'))
    for f in error_files:
        print(f"  - {f.name}")


def main():
    """主函数"""
    import argparse
    
    parser = argparse.ArgumentParser(description='错误处理构建器')
    parser.add_argument('--action', type=str, default='generate',
                       choices=['generate', 'list'],
                       help='操作类型')
    parser.add_argument('--domain', type=str, default='',
                       help='领域名称')
    parser.add_argument('--output', type=str, default='',
                       help='输出目录')
    
    args = parser.parse_args()
    
    project_root = get_project_root()
    output_dir = Path(args.output) if args.output else None
    
    if args.action == 'generate':
        # 检查依赖
        check_dependencies()
        
        # 验证项目结构
        if not validate_project_structure(project_root):
            sys.exit(1)
        
        # 生成错误处理器
        generate_error_handler(output_dir, project_root)
        
        # 生成错误类型
        if args.domain:
            generate_error_types(args.domain, output_dir, project_root)
        
        ColorLogger.success("错误处理构建完成!")
        
    elif args.action == 'list':
        list_error_types(project_root)


if __name__ == '__main__':
    main()
